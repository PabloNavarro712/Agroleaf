private void registerUser(String username, String password, String fullname, String email) {
    try {
        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/agroleaf_db", "root", "");
        String sql = "INSERT INTO users (username, password, fullname, email) VALUES (?, ?, ?, ?)";
        PreparedStatement pst = conn.prepareStatement(sql);
        pst.setString(1, username);
        pst.setString(2, password);
        pst.setString(3, fullname);
        pst.setString(4, email);
        int rowAffected = pst.executeUpdate();
        System.out.println("Fila afectada: " + rowAffected);
  if (rowAffected > 0) {
            JOptionPane.showMessageDialog(this, "Usuario registrado exitosamente.");
            new MainForm().setVisible(true);
            this.dispose(); // Cerrar la ventana de registro
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar el usuario.");
        }
        
        conn.close();
    } catch (Exception e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
    }
    }//GEN-LAST:event_jButtonRegisterActionPerformed

